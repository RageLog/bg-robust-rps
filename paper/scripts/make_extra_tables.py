# ------------------------------------------------------------- T13
def t13_full_generalization_gap():
    """Pick best per (Backbone, Ablation) pair on zero-shot accuracy."""
    df = pd.read_csv(Q1 / "Q1_Table4_Generalization_Gap.csv", sep=";")
    for col in ["İç Veri: Mean Acc", "Dış Veri: Zero-Shot Acc",
                "Generalization Gap"]:
        df[col] = df[col].str.rstrip("%").astype(float)
    sub = (df.sort_values("Dış Veri: Zero-Shot Acc", ascending=False)
             .drop_duplicates(["Model", "Ablasyon"]))
    sub = sub.sort_values(["Model", "Ablasyon"]).reset_index(drop=True)
    rows = []
    for _, r in sub.iterrows():
        rows.append(
            f"{latex_escape(r['Model'])} & {latex_escape(r['Ablasyon'])} & "
            f"{latex_escape(r['Head'])} & {latex_escape(r['Tune'])} & "
            f"{r['İç Veri: Mean Acc']:.2f} & "
            f"{r['Dış Veri: Zero-Shot Acc']:.2f} & "
            f"{r['Generalization Gap']:+.2f} \\\\"
        )

    body = r"""% Auto-generated by paper/scripts/make_extra_tables.py
\begin{table*}[t]
\centering
\caption{Best (by HaGRID zero-shot accuracy) head/tune setting for
every (backbone, ablation) pair: in-distribution accuracy, zero-shot
accuracy, and generalisation gap (\%, percentage points). Sorted
alphabetically by backbone then ablation.}
\label{tab:gen_gap_full}
\begin{tabular}{llllccc}
\toprule
Backbone & Ablation & Head & Tune & In-dist.\ (\%) & Zero-shot (\%) & Gap (pp) \\
\midrule
""" + "\n".join(rows) + r"""
\bottomrule
\end{tabular}
\end{table*}
"""
    write(OUT / "T13_full_generalization_gap.tex", body)


