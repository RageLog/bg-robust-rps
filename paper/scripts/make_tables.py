# ---------------------------------------------------------------- T08
def t08_synthetic_significance():
    df = pd.read_csv(STATS / "pairwise_results.csv")
    sub = df[df["family"] == "A_synth_vs_no_synth"].copy()
    summary = sub.groupby("model").agg(
        n_pairs=("ablation_a", "count"),
        mean_delta=("delta", "mean"),
        max_delta=("delta", "max"),
        min_delta=("delta", "min"),
        mean_d=("hedges_g", "mean"),
        sig_count=("sig_q05", "sum"),
    ).reset_index()
    rows = []
    for _, r in summary.iterrows():
        rows.append(
            f"{latex_escape(r['model'])} & {int(r['n_pairs'])} & "
            f"${r['min_delta']*100:+.2f}$ & ${r['max_delta']*100:+.2f}$ & "
            f"${r['mean_delta']*100:+.2f}$ & ${r['mean_d']:+.2f}$ & "
            f"{int(r['sig_count'])}/{int(r['n_pairs'])} \\\\"
        )

    body = r"""% Auto-generated by paper/scripts/make_tables.py
\begin{table}[t]
\centering
\caption{Effect of the synthetic-augmentation pipeline against the
no-augmentation baseline (NO\_SYNTH), aggregated across all augmentation
modes per backbone. ``Sig.'' counts pairs with BH-FDR $q<0.05$ on the
paired $t$-test. The NO\_SYNTH baseline collapses to $\approx$45--57\%
accuracy in our 4-class setup, while every augmented mode reaches
$\geq$80\%. Note: primary $p$-value is paired $t$-test; Wilcoxon at the $n{=}5$ two-sided floor ($\approx$0.0625) reported in supplementary CSV.}
\label{tab:synth_significance}
\begin{tabular}{lrccccc}
\toprule
Backbone & $n$ pairs & Min $\Delta$ (pp) & Max $\Delta$ (pp) & Mean $\Delta$ (pp) & Mean $g$ & Sig.\ ($q<.05$) \\
\midrule
""" + "\n".join(rows) + r"""
\bottomrule
\end{tabular}
\end{table}
"""
    write_table(OUT / "T08_synthetic_significance.tex", body)


