def t06_tune_strategy_effect():
    df = pd.read_csv(STATS / "pairwise_results.csv")
    sub = df[df["family"] == "C_tune_standard_vs_progressive"].copy()
    rows = []
    for _, r in sub.sort_values("model").iterrows():
        sig = "$^{\\dagger}$" if r["sig_q05"] else ""
        rows.append(
            f"{latex_escape(r['model'])} & {latex_escape(r['ablation'])} & "
            f"${r['mean_standard']*100:.2f}$ & ${r['mean_progressive']*100:.2f}$ & "
            f"${r['delta']*100:+.2f}$ & ${r['hedges_g']:+.2f}$ & "
            f"${r['t_p_raw']:.4f}${sig} \\\\"
        )

    body = r"""% Auto-generated by paper/scripts/make_tables.py
\begin{table*}[t]
\centering
\caption{Effect of progressive vs.\ standard fine-tuning, paired across the
same 5 image-grouped folds (per-pair test: paired $t$-test; $^{\dagger}$
denotes BH-FDR adjusted $q<0.05$ within this family). Positive $\Delta$ means
standard fine-tuning is better. The two-sided minimum achievable $p$-value
for paired Wilcoxon at $n{=}5$ is $\approx 0.0625$, so the $t$-test is used
as the primary inferential statistic; full Wilcoxon results are in the
supplementary CSV.}
\label{tab:tune_effect}
\begin{tabular}{llccccc}
\toprule
Backbone & Ablation & Standard (\%) & Progressive (\%) & $\Delta$ (pp) & Hedges' $g$ & $p$ ($t$) \\
\midrule
""" + "\n".join(rows) + r"""
\bottomrule
\end{tabular}
\end{table*}
"""
    write_table(OUT / "T06_tune_strategy_effect.tex", body)


