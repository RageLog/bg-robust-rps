# ---------------------------------------------------------------- T05
def t05_generalization_gap():
    df = pd.read_csv(Q1 / "Q1_Table4_Generalization_Gap.csv", sep=";")
    # Strip percent signs and convert
    for col in ["İç Veri: Mean Acc", "Dış Veri: Zero-Shot Acc", "Generalization Gap"]:
        df[col] = df[col].str.rstrip("%").astype(float)
    # Pick representative subset: best per backbone × FULL/standard/standard
    sub = df[(df["Ablasyon"] == "FULL") & (df["Head"] == "Standard")].copy()
    sub = sub.sort_values("Dış Veri: Zero-Shot Acc", ascending=False).drop_duplicates("Model")
    rows = []
    for _, r in sub.iterrows():
        rows.append(
            f"{latex_escape(r['Model'])} & {latex_escape(r['Tune'])} & "
            f"{r['İç Veri: Mean Acc']:.2f} & {r['Dış Veri: Zero-Shot Acc']:.2f} & "
            f"{r['Generalization Gap']:+.2f} \\\\"
        )

    body = r"""% Auto-generated by paper/scripts/make_tables.py
\begin{table}[t]
\centering
\caption{Internal vs.\ external (HaGRID zero-shot) accuracy on the FULL pipeline,
standard head. Negative gap indicates a large drop on the unseen domain
and is the central reviewer-relevant signal of synthetic-to-real distribution
shift. Full results across every (backbone, ablation) cell appear in Table~\ref{tab:gen_gap_full}.}
\label{tab:gen_gap}
\begin{tabular}{llccc}
\toprule
Backbone & Tune & In-dist.\ (\%) & Zero-shot (\%) & Gap (pp) \\
\midrule
""" + "\n".join(rows) + r"""
\bottomrule
\end{tabular}
\end{table}
"""
    write_table(OUT / "T05_generalization_gap.tex", body)


