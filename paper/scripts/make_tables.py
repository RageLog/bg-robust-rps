"""
make_tables.py — Generate IEEE-style booktabs LaTeX tables.

Inputs : 007_public_repo_v2/study/reports/Q1_Reports/Q1_Table*.csv
         paper/stats/per_config_summary.csv
         paper/stats/pairwise_results.csv

Outputs: paper/tables/T01_dataset_inventory.tex
         paper/tables/T02_model_complexity.tex
         paper/tables/T03_main_results.tex
         paper/tables/T04_ablation_impact.tex
         paper/tables/T05_generalization_gap.tex
         paper/tables/T06_tune_strategy_effect.tex
         paper/tables/T07_head_strategy_effect.tex
         paper/tables/T08_synthetic_significance.tex

All tables use \\toprule \\midrule \\bottomrule (booktabs), no vertical lines,
ready for IEEEtran via \\usepackage{booktabs}.
"""
from __future__ import annotations
from pathlib import Path
import pandas as pd
import numpy as np
import json

ROOT = Path(__file__).resolve().parents[1]
Q1 = ROOT.parent / "007_public_repo_v2" / "study" / "reports" / "Q1_Reports"
STATS = ROOT / "stats"
OUT = ROOT / "tables"
OUT.mkdir(parents=True, exist_ok=True)

INV = json.loads((ROOT / "data_provenance" / "raw_dataset_inventory.json").read_text())


def latex_escape(s: str) -> str:
    return str(s).replace("_", r"\_").replace("%", r"\%").replace("&", r"\&")


def write_table(path: Path, body: str):
    path.write_text(body, encoding="utf-8")
    print(f"[SAVED] {path}  ({path.stat().st_size} bytes)")


# ---------------------------------------------------------------- T01
def t01_dataset_inventory():
    cls = INV["samples_per_class"]
    total = INV["total_raw_images"]
    rows = []
    for c, n in cls.items():
        rows.append(f"{latex_escape(c)} & {n:,} & {n/total*100:.1f} \\\\")
    rows.append(f"\\midrule\nTotal & {total:,} & 100.0 \\\\")

    body = r"""% Auto-generated by paper/scripts/make_tables.py
\begin{table}[t]
\centering
\caption{Raw image inventory (pre-synthesis) sourced from a combined Kaggle
RPS corpus and a HaGRID-30k slice. The \texttt{none} class is generated
downstream from background scenes and is therefore not present in raw.}
\label{tab:dataset_inventory}
\begin{tabular}{lrr}
\toprule
Class & Image count & Share (\%) \\
\midrule
""" + "\n".join(rows) + r"""
\bottomrule
\end{tabular}
\end{table}
"""
    write_table(OUT / "T01_dataset_inventory.tex", body)


# ---------------------------------------------------------------- T02
def t02_model_complexity():
    df = pd.read_csv(Q1 / "Q1_Table1_Model_Complexity.csv", sep=";")
    # Reduce to architecture × params (ignore tune strategy duplicates)
    df["params_M"] = df["Eğitilebilir Parametre Sayısı (Milyon)"].str.rstrip("M").astype(float)
    arch = df.groupby("Mimarisi")["params_M"].first().reset_index()
    arch = arch.rename(columns={"Mimarisi": "Backbone", "params_M": "Trainable Params (M)"})
    arch = arch.sort_values("Trainable Params (M)").reset_index(drop=True)

    rows = "\n".join(
        f"{latex_escape(r.Backbone)} & {r['Trainable Params (M)']:.2f} \\\\"
        for _, r in arch.iterrows()
    )

    body = r"""% Auto-generated by paper/scripts/make_tables.py
\begin{table}[t]
\centering
\caption{Trainable-parameter footprint of each backbone (head excluded).
Disk size varies by checkpoint format and is omitted for clarity.}
\label{tab:model_complexity}
\begin{tabular}{lr}
\toprule
Backbone & Trainable params (M) \\
\midrule
""" + rows + r"""
\bottomrule
\end{tabular}
\end{table}
"""
    write_table(OUT / "T02_model_complexity.tex", body)


# ---------------------------------------------------------------- T03
def t03_main_results():
    """Main result: per-backbone, FULL ablation, standard head, both tunes."""
    df = pd.read_csv(STATS / "per_config_summary.csv")
    cur = df[(df["ablation"] == "FULL") & (df["head"] == "standard")].copy()
    pivot = cur.pivot_table(index="model", columns="tune",
                            values=["acc_mean", "acc_std", "macro_f1_mean", "n_folds"],
                            aggfunc="first")
    rows = []
    for model in ["EfficientNetV2B0", "MobileNetV3Small", "ResNet50",
                  "DenseNet121", "VGG16"]:
        if model not in pivot.index:
            continue
        s_mean = pivot.loc[model, ("acc_mean", "standard")] * 100
        s_std = pivot.loc[model, ("acc_std", "standard")] * 100
        p_mean = pivot.loc[model, ("acc_mean", "progressive")] * 100
        p_std = pivot.loc[model, ("acc_std", "progressive")] * 100
        f1_s = pivot.loc[model, ("macro_f1_mean", "standard")] * 100
        f1_p = pivot.loc[model, ("macro_f1_mean", "progressive")] * 100
        n_std = int(pivot.loc[model, ("n_folds", "standard")]) if not pd.isna(pivot.loc[model, ("n_folds", "standard")]) else 0
        n_prog = int(pivot.loc[model, ("n_folds", "progressive")]) if not pd.isna(pivot.loc[model, ("n_folds", "progressive")]) else 0
        flag_s = "$^{*}$" if n_std < 5 else ""
        flag_p = "$^{*}$" if n_prog < 5 else ""
        rows.append(
            f"{latex_escape(model)} & "
            f"${s_mean:.2f} \\pm {s_std:.2f}$ & ${f1_s:.2f}$ & {n_std}{flag_s} & "
            f"${p_mean:.2f} \\pm {p_std:.2f}$ & ${f1_p:.2f}$ & {n_prog}{flag_p} \\\\"
        )

    body = r"""% Auto-generated by paper/scripts/make_tables.py
\begin{table*}[t]
\centering
\caption{Main in-distribution results on the FULL synthetic-augmentation pipeline,
standard classification head, image-grouped 5-fold cross-validation
(mean$\pm$std on validation accuracy, macro-F1 in \%); cells with n$<$5 are flagged~$^{*}$.}
\label{tab:main_results}
\begin{tabular}{lccrccc}
\toprule
 & \multicolumn{3}{c}{Standard fine-tuning} & \multicolumn{3}{c}{Progressive fine-tuning} \\
\cmidrule(lr){2-4}\cmidrule(lr){5-7}
Backbone & Acc.\ (\%) & Macro-F1 & Std $n$ & Acc.\ (\%) & Macro-F1 & Prog $n$ \\
\midrule
""" + "\n".join(rows) + r"""
\bottomrule
\end{tabular}
\end{table*}
"""
    write_table(OUT / "T03_main_results.tex", body)


